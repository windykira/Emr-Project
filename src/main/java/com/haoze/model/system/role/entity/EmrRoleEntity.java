package com.haoze.model.system.role.entity;

import com.haoze.common.model.BaseEntity;

import java.math.BigDecimal;
import java.util.List;

/**
 * 角色信息。
 * @author maxl
 * @time 2018-05-09。
 */
public class EmrRoleEntity extends BaseEntity{

    private String roleOrganization;//所属机构
    private String roleCode;//角色编码
    private String roleName;//角色名称
    private String note;//备注
    private BigDecimal dataVersion;//数据版本
    private String pyCode;//拼音码
    private String wbCode;//五笔码

    private List<String> menuIDs;//关联菜单集合

    public List<String> getMenuIDs() {
        return menuIDs;
    }

    public void setMenuIDs(List<String> menuIDs) {
        this.menuIDs = menuIDs;
    }

    public String getRoleOrganization() {
        return roleOrganization;
    }

    public void setRoleOrganization(String roleOrganization) {
        this.roleOrganization = roleOrganization;
    }

    public String getRoleCode() {
        return roleCode;
    }

    public void setRoleCode(String roleCode) {
        this.roleCode = roleCode;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public BigDecimal getDataVersion() {
        return dataVersion;
    }

    public void setDataVersion(BigDecimal dataVersion) {
        this.dataVersion = dataVersion;
    }

    public String getPyCode() {
        return pyCode;
    }

    public void setPyCode(String pyCode) {
        this.pyCode = pyCode;
    }

    public String getWbCode() {
        return wbCode;
    }

    public void setWbCode(String wbCode) {
        this.wbCode = wbCode;
    }
}
