package com.haoze.model.kb.symp.entity;

import com.haoze.common.model.BaseEntity;

/**
 * 症状实体类。
 * @author yangyb
 * @time 2018-05-11。
 */
public class SympEntity extends BaseEntity {

	/**
	 * 
	 */
	private String organizationID;//所属机构
    private String sympCode;//症状编码
    private String sympName;//症状名称
    private String pyCode;//拼音码
    private String wbCode;//五笔码
    private String range;//使用范围
    
	public String getOrganizationID() {
		return organizationID;
	}
	public void setOrganizationID(String organizationID) {
		this.organizationID = organizationID;
	}
	public String getSympCode() {
		return sympCode;
	}
	public void setSympCode(String sympCode) {
		this.sympCode = sympCode;
	}
	public String getSympName() {
		return sympName;
	}
	public void setSympName(String sympName) {
		this.sympName = sympName;
	}
	public String getPyCode() {
		return pyCode;
	}
	public void setPyCode(String pyCode) {
		this.pyCode = pyCode;
	}
	public String getWbCode() {
		return wbCode;
	}
	public void setWbCode(String wbCode) {
		this.wbCode = wbCode;
	}
	public String getRange() {
		return range;
	}
	public void setRange(String range) {
		this.range = range;
	}
    
    
}
